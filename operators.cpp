#include<iostream>
using namespace std;
int main(){
int a=10 ,b=6;
cout<<"The value of a+b is: "<<a+b<<endl;
cout<<"The value of a-b is: "<<a-b<<endl;
cout<<"The value of a*b is: "<<a*b<<endl;
cout<<"The value of a/b is: "<<a/b<<endl;
cout<<"The value of a%b is: "<<a%b<<endl;
cout<<endl;
cout<<"The value of a++ is: "<<a++<<endl;
cout<<"The value of a-- is: "<<a--<<endl;
cout<<"The value of ++a is: "<<++a<<endl;
cout<<"The value of --a is: "<<--a<<endl;
cout<<endl;
cout<<"The value of a+=b is: "<<(a+=b)<<endl;
cout<<"The value of a-=b is: "<<(a-=b)<<endl;
cout<<"The value of a*=b is: "<<(a*=b)<<endl;
cout<<"The value of a/=b is: "<<(a/=b)<<endl;
cout<<"The value of a%=b is: "<<(a%=b)<<endl;
return 0;
}